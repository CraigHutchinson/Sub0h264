seen = {}
for peek in range(256):
    byte = table[peek]
    tc = byte >> 5
    t1 = (byte >> 3) & 3
    size = (byte & 7) + 1
    key = (tc, t1)
    if key not in seen:
        code = peek >> (8 - size)
        seen[key] = (code, size)

our_codes = [
    [1, 7, 4, 3, 2], [0, 1, 6, 3, 3], [0, 0, 1, 2, 2], [0, 0, 0, 5, 0],
]
our_sizes = [
    [2, 6, 6, 6, 6], [0, 1, 6, 7, 8], [0, 0, 3, 7, 8], [0, 0, 0, 6, 7],
]

print("Chroma DC coeff_token differences (ours vs libavc):")
diffs = 0
for t1 in range(4):
    for tc in range(5):
        if t1 > tc: continue
        key = (tc, t1)
        if key not in seen: continue
        nc, ns = seen[key]
        oc, os_ = our_codes[t1][tc], our_sizes[t1][tc]
        if nc != oc or ns != os_:
            diffs += 1
            print(f"  tc={tc} t1={t1}: ours=({oc},{os_}) libavc=({nc},{ns})")
if diffs == 0:
    print("  All match!")
else:
    print(f"\n  Total: {diffs} differences")
