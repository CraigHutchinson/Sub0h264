def unpack(entry):
    """Unpack a cCabacTable entry into (rangeLPS, nextMPS, nextLPS)."""
    range_lps = entry & 0xFF
    next_mps = (entry >> 8) & 0x7F
    next_lps = (entry >> 15) & 0x7F
    return range_lps, next_mps, next_lps

def compute_expected_next_state(pStateIdx, mps):
    """Compute expected combined_state for MPS and LPS transitions."""
    next_mps_state = TRANS_MPS[pStateIdx]
    next_mps_mps = mps
    next_mps_combined = (next_mps_mps << 6) | next_mps_state

    next_lps_state = TRANS_LPS[pStateIdx]
    next_lps_mps = mps
    if pStateIdx == 0:
        next_lps_mps = 1 - mps  # MPS toggle at state 0
    next_lps_combined = (next_lps_mps << 6) | next_lps_state

    return next_mps_combined, next_lps_combined

range_errors = 0
mps_errors = 0
lps_errors = 0
total_checked = 0

for combined_state in range(128):
    pStateIdx = combined_state & 0x3F
    mps = (combined_state >> 6) & 1

    if combined_state == 63 or combined_state == 127:
        # State 63 is special (pStateIdx=63 = terminal state)
        pass

    expected_mps_combined, expected_lps_combined = compute_expected_next_state(pStateIdx, mps)

    for qRange in range(4):
        entry = cCabacTable[combined_state][qRange]
        range_lps, next_mps, next_lps = unpack(entry)
        expected_range = RANGE_TAB_LPS[pStateIdx][qRange]

        total_checked += 1
        if range_lps != expected_range:
            print(f"RANGE MISMATCH: state={combined_state} (p={pStateIdx},mps={mps}) "
                  f"qRange={qRange}: got {range_lps}, expected {expected_range}")
            range_errors += 1

        if next_mps != expected_mps_combined:
            print(f"MPS TRANS MISMATCH: state={combined_state} (p={pStateIdx},mps={mps}) "
                  f"qRange={qRange}: got {next_mps}, expected {expected_mps_combined}")
            mps_errors += 1

        if next_lps != expected_lps_combined:
            print(f"LPS TRANS MISMATCH: state={combined_state} (p={pStateIdx},mps={mps}) "
                  f"qRange={qRange}: got {next_lps}, expected {expected_lps_combined}")
            lps_errors += 1

print(f"\nChecked {total_checked} entries")
